package com.ad.vo;

public class AdItem {

	private String adName;
	private String adPkg;
	private int delay;
	
	
	public String getAdName() {
		return adName;
	}
	public void setAdName(String adName) {
		this.adName = adName;
	}
	public String getAdPkg() {
		return adPkg;
	}
	public void setAdPkg(String adPkg) {
		this.adPkg = adPkg;
	}
	public int getDelay() {
		return delay;
	}
	public void setDelay(int delay) {
		this.delay = delay;
	}
	
	
	
	
}
